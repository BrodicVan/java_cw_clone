/**
 * Sets indicies into the rawData array so that we know 
 * where to look for the data in order to determine each axis.
 * @param _xIndex
 * @param _yIndex
 * @param _zIndex
 */
public void setIndicies (int _xIndex, int _yIndex, int _zIndex){
	xIndex = _xIndex;
	yIndex = _yIndex;
	zIndex = _zIndex;
}