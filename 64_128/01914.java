/**
 * 设置动态分享参数
 * @param title
 * @param imgUrl
 * @param targetUrl
 * @param summary
 */
public TencentShareEntity(String title, String imgUrl, String targetUrl, String summary, String comment){
    this.title=title;
    this.imgUrl=imgUrl;
    this.targetUrl=targetUrl;
    this.summary=summary;
    this.comment=comment;
}